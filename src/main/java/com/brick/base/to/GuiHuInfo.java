package com.brick.base.to;

public class GuiHuInfo {
	
	private String name;
	private int projectCount;
	private double totalPrincipal;
	private double remainingPrincipal;
	private double totalRentMoney;
	private double remainingRentMoney;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getProjectCount() {
		return projectCount;
	}
	public void setProjectCount(int projectCount) {
		this.projectCount = projectCount;
	}
	public double getTotalPrincipal() {
		return totalPrincipal;
	}
	public void setTotalPrincipal(double totalPrincipal) {
		this.totalPrincipal = totalPrincipal;
	}
	public double getRemainingPrincipal() {
		return remainingPrincipal;
	}
	public void setRemainingPrincipal(double remainingPrincipal) {
		this.remainingPrincipal = remainingPrincipal;
	}
	public double getTotalRentMoney() {
		return totalRentMoney;
	}
	public void setTotalRentMoney(double totalRentMoney) {
		this.totalRentMoney = totalRentMoney;
	}
	public double getRemainingRentMoney() {
		return remainingRentMoney;
	}
	public void setRemainingRentMoney(double remainingRentMoney) {
		this.remainingRentMoney = remainingRentMoney;
	}
	
}
